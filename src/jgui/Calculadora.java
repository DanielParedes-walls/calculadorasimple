    private void jButtonACActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonACActionPerformed
        valorAcumulado = 0;
        textoPantalla.setText("0");
        fase = Fase.cero;
        fasePantalla = FasePantalla.mostrar;
        if (operación != null) {
            operación.jButton.setBackground(opColor);
            operación = null;
        }
        if (equalIsOn) {
            this.jButtonEqual.setBackground(opColor);
            this.equalIsOn = false;
        }
    }//GEN-LAST:event_jButtonACActionPerformed

    private void jButtonMultActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonMultActionPerformed
        double valor = Double.parseDouble(textoPantalla.getText());
        // 2 a 3
        if(fase == Fase.uno) {
            operación = Operación.multiplicación;
            valorAcumulado = valor;
            fase = Fase.dos;
            fasePantalla = FasePantalla.mostrar;
            textoPantalla.setText(Double.toString(valorAcumulado));
            blink(jButtonMult);
            // 6 a 3
            if (equalIsOn) {
                this.jButtonEqual.setBackground(opColor);
                this.equalIsOn = false;
            }
        }
        // 4 a 7
        else if (fase == Fase.dos && fasePantalla == FasePantalla.escribir) {
            valorAcumulado = operación.operación.opera(valorAcumulado, valor);
            operación = Operación.multiplicación;
            fase = Fase.uno;
            fasePantalla = FasePantalla.mostrar;
            textoPantalla.setText(Double.toString(valorAcumulado));
        }
    }//GEN-LAST:event_jButtonMultActionPerformed

    private void jButtonEqualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEqualActionPerformed
        // 2 a 6
        if (operación == null && valorAcumulado == 0) {
            double valor = Double.parseDouble(textoPantalla.getText());
            valorAcumulado = valor;
            fase = Fase.uno;
            blink(jButtonEqual); equalIsOn = true;
            fasePantalla = FasePantalla.mostrar;
            textoPantalla.setText(Double.toString(valorAcumulado));
        } // 6 a 2
        else if(operación == null && valorAcumulado != 0) {
            valorAcumulado = 0;
            fase = Fase.uno;
            blink(jButtonEqual); equalIsOn = true;
            fasePantalla = FasePantalla.escribir;
        } // 3 a 8, 7 a 6
        else if(fase == Fase.dos && fasePantalla == FasePantalla.mostrar) {
            fase = Fase.uno;
            operación.jButton.setBackground(opColor); operación = null;
            blink(jButtonEqual); equalIsOn = true;
        } // 4 a 5
        else if(fase == Fase.dos && fasePantalla == FasePantalla.escribir) {
            double valor = Double.parseDouble(textoPantalla.getText());
            valorAcumulado = operación.operación.opera(valorAcumulado, valor);
            operación = null;
            fasePantalla = FasePantalla.mostrar;
            textoPantalla.setText(Double.toString(valorAcumulado));
            fase = Fase.uno;
            blink(jButtonEqual); equalIsOn = true;
        }
    }//GEN-LAST:event_jButtonEqualActionPerformed

    private void jButtonNumber(java.awt.event.ActionEvent evt, javax.swing.JButton jButton) {
        if (textoPantalla.getText().equals("0") || fasePantalla == FasePantalla.mostrar) {
            textoPantalla.setText(jButton.getText());
            fasePantalla = FasePantalla.escribir;
            if (operación != null) {
                operación.jButton.setBackground(opColor);
            }
            if (fase == Fase.cero) fase = Fase.uno;
            else if (fase == Fase.uno && operación != null) {
                fase = Fase.dos;
            }
            // 6 a 2
            else if (fase == Fase.uno && operación == null) {
                valorAcumulado = 0;
                fasePantalla = FasePantalla.escribir;
            }
        } else {
            textoPantalla.setText(textoPantalla.getText() + jButton.getText());
        }
        
        if (equalIsOn) {
            this.jButtonEqual.setBackground(opColor);
            this.equalIsOn = false;
        }
    }
    
    /* Hace parpadear la pantalla. */
    private void blink(JButton jButton) {
        jButton.setBackground(Color.magenta);
    }
    
