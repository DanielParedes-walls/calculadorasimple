    @FunctionalInterface
    private interface OperaciónBinaria {
        public double opera(double n1, double n2);
    }
    
    private enum Operación {
        multiplicación,
        suma,
        resta,
        división;
        
        javax.swing.JButton jButton;
        OperaciónBinaria operación;
        
        void setAttributes(javax.swing.JButton jButton, OperaciónBinaria op) {
            this.jButton = jButton;
            this.operación = op;
        }
    }
    
    /* La calculadora procesa y almacena datos en fases. */
    private enum Fase {
        cero,  // Cero inicial
        uno,   // Introducción del primer operando
        dos    // Introducción del segundo operando, o posteriores.
    }
    
